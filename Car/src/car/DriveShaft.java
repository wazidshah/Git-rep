package car;

public class DriveShaft {

	private int DriveShaftNo;
	private double price;
	
	public DriveShaft(int driveShaftNo, double price) {
		super();
		DriveShaftNo = driveShaftNo;
		this.price = price;
	}

	public int getDriveShaftNo() {
		return DriveShaftNo;
	}

	public double getPrice() {
		return price;
	}
	
	
	
}
